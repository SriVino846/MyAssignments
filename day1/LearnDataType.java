package week1.day1;

public class LearnDataType {
	
	public static void main(String[] args) {
		
		//integer
		
		//Primitive Data Type
		
		byte age = 32;
		System.out.println(age);
		
		int noOfLearners = 1234567892;
		
		long phNo=123456789L;
		
		short regNo = 12345;
		
		//Non-Primitive Data Type
		
		String name = "Sri";
		System.out.println(name);
	}

}
